package ch1;

public class VariableEx1 {

	public static void main(String[] args) {
		// 변수 : 메모리 공간

		// 자바 문장의 끝은 세미콜론으로 끝나야 함
		int a = 10, b = 5;

		System.out.println("a+b");
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);

	}

}
